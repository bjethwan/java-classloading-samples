package com.bjethwan;

public class Config {
	private String factoryJar;
	private String factoryClass;
	
	public Config(){
		
	}
	
	public String getFactoryJar() {
		return factoryJar;
	}
	public void setFactoryJar(String factoryJar) {
		this.factoryJar = factoryJar;
	}
	public String getFactoryClass() {
		return factoryClass;
	}
	public void setFactoryClass(String factoryClass) {
		this.factoryClass = factoryClass;
	}
}
